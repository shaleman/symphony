func (s *Schema) GenerateJs() (string, error) {
	var goStr string
	var buf bytes.Buffer

	const hdr = `// {{.}}.js
// This file is auto generated by modelgen tool
// Do not edit this file manually
`

	// Generate file headers
	tmpl, _ := template.New("hdrTmpl").Parse(hdr)
	tmpl.Execute(&buf, s.Name)
	goStr = goStr + buf.String()

	//  Generate all object Views
	for _, obj := range s.Objects {
		objStr, err := obj.GenerateJsViews()
		if err == nil {
			goStr = goStr + objStr
		}
	}

	return goStr, nil
}

